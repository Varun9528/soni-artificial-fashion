// Function to translate a single word
function translateWord(word: string): string {
  // Convert to lowercase for matching
  const lowerWord = word.toLowerCase();
  
  // Check if we have a direct translation
  if (basicTranslations[lowerWord]) {
    return basicTranslations[lowerWord];
  }
  
  // If no direct translation, return the original word
  return word;
}

// Function to translate a phrase by breaking it into words
function translatePhrase(phrase: string): string {
  if (!phrase || typeof phrase !== 'string') {
    return phrase;
  }
  
  // Split the phrase into words, preserving spaces and punctuation
  const words = phrase.split(/(\s+)/);
  
  // Translate each word
  const translatedWords = words.map(word => {
    // If it's just whitespace, keep it as is
    if (/^\s+$/.test(word)) {
      return word;
    }
    
    // Check for direct phrase translation first
    const lowerPhrase = word.toLowerCase();
    if (basicTranslations[lowerPhrase]) {
      return basicTranslations[lowerPhrase];
    }
    
    // Otherwise, translate individual words
    return translateWord(word);
  });
  
  return translatedWords.join('');
}

// Main translation function
export function translateToHindi(text: string): string {
  if (!text || typeof text !== 'string') {
    return text;
  }
  
  // For very short texts, try direct translation first
  const lowerText = text.toLowerCase().trim();
  if (lowerText.length <= 20 && basicTranslations[lowerText]) {
    return basicTranslations[lowerText];
  }
  
  // For longer texts, break into phrases and translate
  return translatePhrase(text);
}

